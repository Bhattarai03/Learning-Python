# Loop in Python

# While loop
i=1
while(i<3):
    print(i)
    i +=1 

# Example of while loop
a=1
while(a<3):
    print("I Love You")
    a+=1

# List using while loop

a=["harry","rohit","Aakash","Vac","Raman"]
b=len(a)
c=0
while(c<b):
    print(a[c])
    c+=1

# For loop
for i in range(4):
    print(i)

# Range function
 
for j in range(0,100,4):  #It is used to generate range in the number of sequence 
    print(j)


